<div class="title">
  The life cycle of a shrimp
</div>
<div class="sub-title">
  Based on their mode of life, the Life Cycle of Shrimp can be divided into the following progress:
</div>
<div class="block2-4"></div>
<div class="block3-5">
  <div class="arrow3"></div>
  <div class="arrow5"></div>
</div>
<div class="block2-6">
  <div class="arrow-left"></div>
</div>
<fieldset class="block1">
  <legend>Mated Female</legend>
  <div class="desc">
    This mated female has sperm attached to her. When she's ready, she will release her eggs from her ovipore, and the eggs will bevome instantly fertilized and released into the water. This is called spawning. Spawned eggs look like thousands of tiny white specks.
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block2">
  <legend>Unhatched Eggs</legend>
  <div class="desc">
    The nauplii will grow inside of the egg. Once the eggs hatch, they will go through a series of larval stages before they become a full formed shrimp.
  </div>
</fieldset>
<fieldset class="block3">
  <legend>Nauplii</legend>
  <div class="desc">
    After eggs hatch, the first larval stage the shrimp go through is "nauplii".
  </div>
</fieldset>
<fieldset class="block4">
  <legend>Mysis</legend>
  <div class="desc">
    The third larval stage the shrimp go through is "mysis". At this stage the larval shrimp begin to develop its pleopods, or swimmerettes.
  </div>
</fieldset>
<fieldset class="block5">
  <legend>Zoea</legend>
  <div class="desc">
    The larval shrimp begin to eat diatoms (algae) and newly hatched brine shrimp cycsts (Artemia) as their food source. It is during this stage that the larval shrimp develop their eyes and extended body length.
  </div>
</fieldset>
<fieldset class="block6">
  <legend>Grow-out</legend>
  <div class="desc">
    Once the young shrimp grow to about 1 gram, they need a larger space to grow up in, so they are transferred to grow-out ponds. Shrimp are fed a pellet diet for this stage of their life. The average harvested size ranges 21 to 30 grams.
  </div>
  <div class="arrow-right">

  </div>
</fieldset>
<fieldset class="block7">
  <legend>Post-Larvae</legend>
  <div class="arrow-up"></div>
  <div class="desc">
    After completing the 3 stages of larval development, the animals are fully developed as a shrimp. Once they reach about 1 cm long, they are transferred into juvenile ponds to be grown out to about 1 gram. While still in the hatchery, the post larval shrimp eat live baby brine shrimp.
  </div>
</fieldset>
<fieldset class="block8">
  <legend>Broodstock</legend>
  <div class="desc">
    Sometimes we allow the shrimp to grow to full size so they can breed and start the cycle over again.<br>
    When the shrimp are about 9 to 14 months old, they are at their prime for breeding. They are fed a very special diet to ready them for fertilization, which includes frozen squid, frozen blookworms and frozen brine shrimp (Artemia)...
  </div>
</fieldset>
