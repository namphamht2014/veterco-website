<div class="title">
  The life cycle of a buffalo
</div>
<div class="sub-title">
  Raising buffalo includes the following stages:
</div>
<fieldset class="block1">
  <legend>Breeding</legend>
  <div class="desc">
    Gestation period up to 11 months. Female water buffalo produce one water buffalo calf every couple of years.<br>
    The baby water buffalo stays with its mother and is dependant on her for it's first couple of years.
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block2">
  <legend>Puberty</legend>
  <div class="desc">
    Age at puberty ranges from 16-22 to 36-40 months in various countries.<br>
    Puberty is significantly affected by:
    <ul>
      <li>Breed</li>
      <li>Season</li>
      <li>Climate</li>
      <li>Feeding systems</li>
      <li>Growth rate</li>
    </ul>
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block3">
  <legend>Sexual Maturity</legend>
  <div class="desc">
    Females reach sexual maturity at approximately 1.5 years of age.<br>
    Males reach sexual maturity at the age 3 years.<br>
    The calving interval of buffalo varies between 400 and 600 days.
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block4">
  <legend>Full-Maturity</legend>
  <div class="desc">
    Can live up to 25 years in the wild, 29 years in captivity - nearly 3 metres (10 feets) long and 2 metres tall and weighing up to 1,200 kg (2,600 poinds). Females are about two-thirds this size.
  </div>
</fieldset>
