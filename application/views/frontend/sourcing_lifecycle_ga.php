<fieldset class="block2">
  <legend>Hatchery</legend>
  <div class="desc">
    - The hatchery is responsible for the incubation and hatching of chicks from fertile eggs obtained from broiler breeders.<br>
    - Once hatched, chicks are removed from the hatching backets and egg shell debris is discarded.<br>
    - Chicks are counted into trays that are used for delivery to grow-out farms.
  </div>
</fieldset>
