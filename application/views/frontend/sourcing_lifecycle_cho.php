<div class="title">
  The life cycle of a dog-pets
</div>
<div class="sub-title">
  Dogs go through four stages of the life cycle: puppy, adolescent, adult and senior.<br>
  Like humans and other mammals, individual dogs move through the stages of the life cycle at different rates.
</div>
<fieldset class="block1">
  <legend>Puppy</legend>
  <div class="desc">
    During the puppy stage -- both while with his mom and after he comes to you -- Your dog is learning about the proper way to interact with humans, other dogs and othe. During this stage of the life cycle, it's important to begin house training and get your puppy's compete set of vaccinations.
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block2">
  <legend>Adolescent</legend>
  <div class="desc">
    Hormones start to kick in and, if not spayed / neutered, your dog may begin to act like a moody teenager. Your dog will lose his puppy fur and grow to his adult size. At the beginning of this stage of life cycle have your dog altered and consider obedience training.
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block3">
  <legend>Adult</legend>
  <div class="desc">
    During this phase of the life cycle, your dog will still enjoy plenty of exercise and playtime, but he likely won't be so demanding of your attention and will not burst with the same amounts of energy that kept you busy during earlier stages. He is likely completely house-trained by now. Consider obedience training or advanced training. Your adult dog will continue to thrive from the mental and physical exercise of learning.
  </div>
  <div class="block-arrow">- - - -</div>
</fieldset>
<fieldset class="block4">
  <legend>Senior</legend>
  <div class="desc">
    Unlike with adolescence and adulthood, larger breed dogs reach this stage of the life cycle at a younger age than smaller breed dogs. During this stage of the life cycle your dog's muzzle will likely begin to gray. He will move more slowly and, instead of a brisk game of fetch, he'll be more content with a stroll. Your senior dog may eat less and sleep more than he has at other times of his life.
  </div>
</fieldset>
